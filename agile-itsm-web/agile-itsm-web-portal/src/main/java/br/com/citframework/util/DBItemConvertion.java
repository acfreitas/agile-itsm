package br.com.citframework.util;

public class DBItemConvertion {
	private String nameToBeConverted;
	private String nameAfterConversion;
	public String getNameToBeConverted() {
		return nameToBeConverted;
	}
	public void setNameToBeConverted(String nameToBeConverted) {
		this.nameToBeConverted = nameToBeConverted;
	}
	public String getNameAfterConversion() {
		return nameAfterConversion;
	}
	public void setNameAfterConversion(String nameAfterConversion) {
		this.nameAfterConversion = nameAfterConversion;
	}
}
